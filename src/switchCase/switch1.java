package switchCase;

public class switch1 {

	public static void main(String[] args) {
     
		int i=20;
		switch(i)
		{
		case 10:
		System.out.println("i am 10");
		break;
		
		case 20:
		System.out.println("i am 20");
		break;
		
		case 30:
		System.out.println("i am 30");
		break;
		
		default:
			System.out.println("this code will execute if no one match");
		}
	}

}
